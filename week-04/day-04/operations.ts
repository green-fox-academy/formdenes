'use strict';

export function addNum(a: number, b: number): number {
  return a + b;
}

export function subtractNum(a: number, b: number): number {
  return a - b;
}

export class NumberOperations {
  public addNum(num1: number, num2: number): number {
    return num1 + num2;
  }

  public subtractNum(num1: number, num2: number): number {
    return num1 + num2;
  }
}